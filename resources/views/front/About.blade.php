		<p>The CommunAbility project envisions diverse and vibrant communities in which all members have the opportunities for personal and social development needed to fully participate, socially and economically, in Canadian society.</p>
		<p>Our aim is to further the work underway in post-secondary education to remove barriers and create opportunities that promote the success of students with disabilities.</p>
